    private void btnJadwalkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJadwalkanActionPerformed
    String nama = inputnamaibu.getText();
    String nama_dokter = pilihnamadokter.getSelectedItem().toString();
    String tanggal = inputtanggalkonsultasi.getText();

    // Validasi input
    if (nama.isEmpty() || tanggal.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Harap isi semua data!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Simpan ke database
    try (Connection conn = database_connection.getConnection()) {
        String query = "INSERT INTO konsultasi (nama_dokter, tanggal, nama) VALUES (?, ?, ?)";
        java.sql.PreparedStatement stmt = conn.prepareStatement(query);
        stmt.setString(1, nama_dokter);
        stmt.setString(2, tanggal);
        stmt.setString(3, nama);
        stmt.executeUpdate();

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tabelJadwalKonsultasi.getModel();
        model.addRow(new Object[]{nama_dokter, tanggal});

        // Reset form input
        inputnamaibu.setText("");
        inputtanggalkonsultasi.setText("");
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }    }//GEN-LAST:event_btnJadwalkanActionPerformed

