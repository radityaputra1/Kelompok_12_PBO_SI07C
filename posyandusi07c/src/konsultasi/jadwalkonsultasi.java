    private void tabelJadwalKonsultasi() {
    try (Connection conn = database_connection.getConnection()) {
        String query = "SELECT nama_dokter, tanggal_konsultasi FROM jadwal_konsultasi";
        java.sql.PreparedStatement stmt = conn.prepareStatement(query);
        var rs = stmt.executeQuery();

        // Bersihkan tabel
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tabelJadwalKonsultasi.getModel();
        model.setRowCount(0);

        // Tambahkan data ke tabel
        while (rs.next()) {
            String namaDokter = rs.getString("nama_dokter");
            String tanggalKonsultasi = rs.getString("tanggal_konsultasi");
            model.addRow(new Object[]{namaDokter, tanggalKonsultasi});
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}   
